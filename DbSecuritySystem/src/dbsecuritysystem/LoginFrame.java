    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username=jTextField1.getText();
        String password=jPasswordField1.getText();
        username=username.trim();
        password=password.trim();
        
        if(username.isEmpty())
            JOptionPane.showMessageDialog(null,"Username is empty");
        else if(password.isEmpty())
             JOptionPane.showMessageDialog(null,"Password is empty");
        else if(password.length()!=8)
             JOptionPane.showMessageDialog(null,"Password should be 8 character");
        else if(jRadioButton1.isSelected()==false && jRadioButton2.isSelected()==false)
            JOptionPane.showMessageDialog(null,"Please confirm who you are ");
        else
        {

            String who="";
            if(jRadioButton1.isSelected())
            {
                 who=jRadioButton1.getText();
                 System.out.println("Usernname = "+username);
                 System.out.println("Password = "+password);
                 System.out.println("Who is = "+who);
                 if(new clientDBOP().isExisted(username, password))
                 {
                     //clientOperationframe.clientstr=clientOperationframe.clientstr+username;
                     clientOperationframe.clientname=username;
                     this.dispose();
                     clientOperationframe cof=new clientOperationframe();
                     cof.setVisible(true);
                     Dimension d= Toolkit.getDefaultToolkit().getScreenSize();
                     cof.setSize(d);
                 }
                 else
                 {
                     JOptionPane.showMessageDialog(null,"User name or password is incorrect");
                 }
                
            }
               
            else
            {
                 System.out.println("Usernname = "+username);
                 System.out.println("Password = "+password);
                 System.out.println("Who is = "+who);
                 
              if(new AdminOP().isAdminExisted(username, password))
              {
                    this.dispose();
                    AdminOperationframe aof=new AdminOperationframe();
                    aof.setVisible(true);
                   Dimension d= Toolkit.getDefaultToolkit().getScreenSize();
                   aof.setSize(d);
              }
              else
                  JOptionPane.showMessageDialog(null,"username or password is incorrect");
                
         }
                
                    
            
            
        }


            
    }//GEN-LAST:event_jButton1ActionPerformed

